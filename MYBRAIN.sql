CREATE TABLE TAG (
    TagId NUMBER GENERATED BY DEFAULT AS IDENTITY,
    Name NVARCHAR2(40),
    
    CONSTRAINT PK_TAG PRIMARY KEY (TagId)
)

CREATE TABLE TODO (
    TodoId NUMBER GENERATED BY DEFAULT AS IDENTITY,
    Title NVARCHAR2(255),
    Color NVARCHAR2(7),
    TagId NUMBER NULL,
    
    CONSTRAINT PK_TODO PRIMARY KEY (TodoId),
    CONSTRAINT FK_TAG FOREIGN KEY (TagID) REFERENCES TAG(TagId)
)

CREATE TABLE TASK (
    TaskId NUMBER GENERATED BY DEFAULT AS IDENTITY,
    Description NVARCHAR2(500),
    Checked NUMBER(1) DEFAULT 0,
    TodoId NUMBER,
    
    CONSTRAINT PK_TASK PRIMARY KEY (TaskId),
    CONSTRAINT FK_TODO FOREIGN KEY (TodoID) REFERENCES TODO(TodoId)
)
